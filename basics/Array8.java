
class Array8
{
	public static void main(String args[])
	{
		
		int arr1[][] = new int[3][];
		int arr2[][]=new int[2][3];
		
		for(int ar[] :arr1)
		{
			System.out.print(" "+ar);

		}
		System.out.println("\n========================");
		for(int ar[] :arr2)
		{
			System.out.print(" "+ar);

		}
		System.out.println("\n========================");
	}
}

class Array4
{
	public static void main(String args[])
	{
		boolean ar1[] = new boolean[3];
		for(boolean a : ar1)
		{
			System.out.println(a);
		}
		int ar2[] = new int[3];
		for(int b : ar2)
		{
			System.out.println(b);
		}
		float ar3[] = new float[3];
		for(float c : ar3)
		{
			System.out.println(c);
		}
	}




}